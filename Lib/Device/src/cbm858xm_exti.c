/**
  * @brief  Deinitializes the EXTI peripheral registers to their default reset values.
  * @param  None
  * @return None
  */
void exti_def_init(void)
{
    EXTI->INTEN = 0x0FF40000;
    EXTI->EVTEN = 0x00000000;
  
    EXTI->RTEN = 0x00000000;
    EXTI->FTEN = 0x00000000;
    EXTI->PDF = 0x00000000;
}

